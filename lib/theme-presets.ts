// 获取主题样式
export function getThemeStyles(name: string): ThemeStyles {
  if(name === "default") {
    return defaultThemeStyles;
  }
  
  const themeStyles = themePresets[name].styles;
  const light = {
    ...defaultLightThemeStyles,
    ...themeStyles.light,
  };
  
  return {
    light,
    dark:{
      ...light,
      ...defaultDarkThemeStyles,
      ...themeStyles.dark,
    }
  }
}
